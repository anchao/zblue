#include <stdio.h>
#include <unistd.h>
#include <common/log.h>

#include "bluetooth/bluetooth.h"
#include "drivers/bluetooth/hci_driver.h"

extern int bt_uart_init(void);

static void bt_ready(int err)
{
	if (err) {
		return;
	}

	if (IS_ENABLED(CONFIG_SETTINGS)) {
		settings_load();
	}
}

int main(void)
{
	int ret;

	ret = bt_uart_init();
	if (ret < 0) {
		return ret;
	}

	return bt_enable(bt_ready);
}
